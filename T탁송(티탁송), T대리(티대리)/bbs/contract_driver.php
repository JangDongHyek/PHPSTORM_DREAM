<p>9. 계약해지 이후 처리 의무:</p>
<p>&nbsp; 1. "을"로 인해 "갑"에게 손실액이 발생 될 경우 "을"은 계약 해지 이후라도 100% 책임을 진다(민, 형사 처벌 동의)</p>
<p>&nbsp; 2. "을"이 타 회사에 등록 되어 있을 경우, "갑"은 손실 금액을 해당 소속 회사에 청구 통보하며, "을"은 해당 회사에서의 캐쉬 인출을 허락한다.</p>
