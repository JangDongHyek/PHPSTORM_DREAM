<p>&nbsp; 4. "을"은 "갑"이 정한 콜 수행 프로그램과 T대리 어플리케이션을 설치하며, 위반 시 "갑"은 "을"과의 계약을 "을"에게 사전 고지 없이 해지 할 권한이 있다.</p>
