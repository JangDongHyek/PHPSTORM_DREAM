function setBbTransURI(uri) {
	var sBbTransURI;

	try {
		sBbTransURI = uri;
	}
	catch(e) {
		sBbTransURI = '';
	}

	if (sBbTransURI == '' || sBbTransURI == '#') {
		return bb_trans_uri;
	}
	else if (sBbTransURI.substr(0,1) == '/') {
		if(sBbTransURI.substr(1, 1) == '/') {
			return "http:" + sBbTransURI;
		}
		else {
			return "" + sBbTransURI;
		}
	}
	else if (/^https?:\/\//.test(sBbTransURI)) {
		return sBbTransURI;
	}
	else {
		return "http://www.usautoparts.net/catalog/?N=0&uts=true&Ntt=a/" + sBbTransURI;
	}
}


