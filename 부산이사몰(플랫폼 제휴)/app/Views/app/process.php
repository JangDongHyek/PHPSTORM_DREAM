                <dt><strong class="txt_color">step 01.</strong> 온라인 이사견적의뢰</dt>
                <dd>고객님께서 사이트에 접속하시어, 견적신청 페이지를 통하여 고객님의 정보와 이사에 관련된 항목들을 작성하시어 신청 하시면 시스템에 저장되고 담당자가 확인하게 됩니다.</dd>
            </dl>
            <dl>
                <dt><strong class="txt_color">step 02.</strong> 회원업체 견적서 문자발송</dt>
                <dd>견적서비스를 이용하시면 고객님의 견적이 회원업체에 문자 발송됩니다.</dd>
            </dl>
            <dl>
                <dt><strong class="txt_color">step 03.</strong> 방문 또는 온라인 계약</dt>
                <dd>업체와 계약이 체결되면 업체에서 계약서를 작성해 드립니다.</dd>
            </dl>
            <dl>
                <dt><strong class="txt_color">step 04.</strong> 계약금지불</dt>
                <dd>계약서를 전송받으시면 이사금액의 10%에 해당하는 금액을 업체에 직접 지불하시면 됩니다.</dd>
            </dl>
            <dl>
                <dt><strong class="txt_color">step 05.</strong> 이사서비스</dt>
                <dd>고객님께서 희망하시는 날짜에 이사팀이 방문하여 이사서비스를 제공하여 드리게 됩니다. 각 이사별 차별화된 서비스와 최고의 서비스로 고객님의 보금자리를 안전하게 이사하여 드립니다.</dd>
            </dl>
            <dl>
                <dt><strong class="txt_color">step 06.</strong> 잔금지불</dt>
                <dd>카드지불인 경우에는 최소 이사일 3일전까지 홈페이지에서 결재를 하셔야하며 현금인 경우에는 이사당일 업체에 직접 지불하시면 됩니다. (합의된 서비스 비용 외의 추가비용을 부당하게 요구하지 않습니다.)</dd>
            </dl>
            <dl>
                <dt><strong class="txt_color">step 07.</strong> 고객평가 및 A/S접수</dt>
                <dd>이사 후 고개님들이 이사서비스 받으신 소감 또는 불편을 후기게시판에서 평가를 해주시면 됩니다.</dd>
                <dd>또한 A/S받을 부분이 있으시면 A/S접수신청에 내역을 남겨주시면 담당자가 바로 연락드리고 서비스해드립니다.</dd>
            </dl>
