        <h3>3. 이사방법 및 이사업체 선정하기</h3>
        <div class="dl_basic">
            <dl>
                <dt><i class="fa-duotone fa-circle-check"></i> 이사 방법</dt>
                <dd>이사방법은 예전, 일가 친척 등 가족 중심으로 소비자가 단순히 운반 수단인 차량만을 용차하여 이삿짐을 운반하던 방법에서 소득 수준 향상으로 인한 편리함 추구로 이사화물을 전문적으로 취급하는 상용인부를 고용, 이삿짐의 포장, 운반, 정리, 정돈 등 부대 서비스를 일괄적으로 제공하는 형태로 발전되었다.</dd>
            </dl>
            <dl>
                <dt><i class="fa-duotone fa-circle-check"></i> 일반이사</dt>
                <dd>소비자가 이삿짐을 직접 포장하고 이사업체에서는 이삿짐의 운송과 반ㆍ출입 업무만 제공하는 형태로 이삿짐의 양이 적거나 저렴하게 이사를 하고자 할때 이용하는 형태</dd>
            </dl>
            <dl>
                <dt><i class="fa-duotone fa-circle-check"></i> 포장이사</dt>
                <dd>포장이사는 이사업체에서 이삿짐을 포장, 하역, 운반, 정리, 정돈 등 일괄 부대서비스를 제공하여 운송하는 형태의 이사를 말한다. 포장이사는 견적 사원이 직접 소비자를 방문, 이사물품의 수량과 이사조건 등을 직접 견적하고 쌍방 합의하에 계약이 이루어짐으로서 이사물품의 파손, 분실 등의 피해가 일반이사에 비해 상대적으로 감소하고 또한 다양한 서비스를 제공받을 수 있는 편리함과 안전성이 있으나 가격은 일반이사보다 비싼 편이다..</dd>
            </dl>
            <dl>
                <dt><i class="fa-duotone fa-circle-check"></i> 이사업체 선정하기</dt>
                <dd>이사업체는 보통 이사 3주일전 선정하는 것이 좋다. 이사업체는 반드시 허가 업체를 선정(협회 안내전화 또는 연합회 홈페이지 참조)하고 이사형태, 이사조건에 따라 2～3개 업체의 견적을 받아 견적내용, 금액, 이사내용에 대한 서비스조건 등을 꼼꼼히 비교 분석한 후 선택하되 동일조건에서 과도한 요금 덤핑이나 상대적으로 터무니없는 서비스를 제시하는 업체는 가급적 피하는 것이 좋다. 이런 업체는 대부분 무허가 업체이거나 일부 사업규모 및 시설이 부실한 업체로서 계약후 전문성이 떨어지고 임금 수준이 낮은 일용근로자나 아르바이트 일부를 동원하여 이사화물을 운반하는 경우가 많아 낮은 가격만 믿고 이삿짐을 의뢰하였다가 낭패를 보는 경우가 허다하기 때문이다..</dd>
            </dl>
