        <h3>6. 이사화물 피해가 발생하면....</h3>

        <h4>이사화물의 특징</h4>
        <i class="fa-duotone fa-circle-check"></i> 이사화물은 일반 화물과 달리 거의가 쉽게 파손되거나 훼손되는 물품이 대부분이다. <br>
        국민소득이 증가함에 따라 고가품, 이삿짐의 양도 증가하고 있으며 이사화물의 품목도 그 만큼 다양화되고 있다.<br>
        따라서 이와 같은 특성으로 인해 이사화물은 아무리 주위를 기울여 포장하고 운반한다 해도 파손, 훼손, 분실되는 사례가 자주 발생하게 된다.
        <br>
        <h4>이삿짐 피해 분쟁 발생의 요인</h4>
        <div>
        <p>계약서 미작성으로 인한 계약조건 변경과 계약 불이행으로 인한 분쟁</p>
        - 계약서를 작성하지 않고 유.무선 상으로만 이사화물운송 계약을 하여, 계약조건(작업조건)변경, 날짜, 시간 등의 불이행으로 인한 분쟁.
        </div>
        <div>
        <p><i class="fa-duotone fa-circle-check"></i> 파손, 훼손, 분실 등의 사전 인지 여부.</p>
        - 이삿짐은 그 품목이 다양하여 직접 현장 견적을 하더라도 이사물품을 모두 견적서에 명시(예 : 장롱서랍의 옷가지, 주방용품 등)<br>
        할 수 없어 분실시 책임소재 규명과, 전자제품의 사전 고장유무(예: 모든 전자제품을 이사전 성능(고장)확인 불가.), 가구 등의
        사전 흠집 파손 유.무 등을 일일이 모두 확인할 수 없어 피해시 분쟁 발생.
        </div>
        <div>
        <i class="fa-duotone fa-circle-check"></i> 일반이사의 경우 이삿짐을 소비자가 직접 포장하여 이사업체에서는 내용물, 내용물의 사전 훼손, 파손 상태를 알 수 없고,<br>
        포장이사의 경우 이사업체에서 포장, 운반, 정리, 정돈 등 일체의 서비스를 제공하므로 피해 발생의 모든 책임을 쌍방 인지하고
        확인하지 않은 경우에는 업체 과실로만 고착시킴으로 피해발생시 분쟁 발생
        </div>
        <br>
        <h4>이사화물 피해가 발생하면....</h4>
        <div>
        이사당일 한창 바쁘게 일하는데 소비자가 이사 물품의 파손, 훼손 등을 발견하기는 쉽지 않다.<br>
        하지만 이사중이라도 이와 같은 사실이 발견되면 즉시 종사원과 현장책임자(작업 반장)에게 확인시키고 작업 종료후 확인서를
        받아두는 것이 좋다. <br> 또한 이사후 피해 물품을 발견했을 경우엔 즉시 이사업체에 피해사실을 통보하고 이사당시의 작업반장,
        사업체 대표자의 확인 절차를 거쳐 적절한 A/S 및 보상을 청구한다.<br>
        <span class="icon icon_gray">이사 당일 사고 유무를 떠나 작업반장 및 작업원의 성명과 전화번호를 기록해 두는 것이 좋은 방법이다.</span>
        </div>
        <br>
        <h4>이사화물 피해 분쟁이 발생하면....</h4>
        <div>
        - 이사화물의 파손, 망실, 훼손, 분실 등의 피해가 발생하고 다행하게도 발생당시 책임 소재가 분명하고 사업자가 과실을 인정하여
        피해를 보상하는 경우엔 문제가 없으나 앞서 열거한 분쟁 요인들의 책임소재에 대한 주장이 서로 달라 피해보상을 거부하거나<br>
        피해(청구) 금액의 차이로 분쟁이 발생하면 부득이 분쟁조정을 신청할 수밖에 없으며 분쟁조정 결과에 따라
        피해보상을 받을 수밖에 없다.<br>
        <span class="icon icon_color">⇒ 분쟁 발생시 소비자는 한국소비자보호원(상담실 3460-3000)에 분쟁조정 신청</span>
