        <h3>1. 이사 동향</h3>
        <div>
        이사는 주로 봄, 가을철에 집중되고 있으나 금년도에는 경기침체와 정부의 부동산 투기 억제 정책 등으로 예년에 비해 이사 빈도수가 급격히 줄어들 것으로 예상된다. 따라서 이사를 계획한 소비자는 저렴한 이사요금으로 양질의 서비스를 제공받을 수 있고 경우에 따라서는 이사업체의 난립과 과당경쟁, 또는 사전준비 소홀로 인해 선의의 피해를 당할 수 있으므로 소비자들의 세심한 주의가 필요하다.
