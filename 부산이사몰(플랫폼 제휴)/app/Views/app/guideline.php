        <h3>5. 이사짐 계약은..</h3>
        <div>
        이사계약은 앞서 열거한 이사형태, 이사비용 등을 충분히 검토한 후 일반이사든 포장이사든 이삿짐의 적고 많음을 떠나 반드시 허가업체와 서면계약서를 작성한다.
        <br>
        방문 견적을 통한 허가업체와의 정확한 계약서를 작성해야만이 계약 불이행으로 인한 손해와 이삿짐 피해 발생시 피해보상을
        받을 수 있기 때문이다. 한편 요즘은 인터넷을 통한 견적과 계약이 종종 이루어지고 있는데 이런 경우에도 허가업체 유무
        <span class="icon icon_gray"> 연합회 홈페이지 www.kffa.or.kr </span>를 확인하고 방문견적이 아닌 관계로 이사전 이사업체의 직원이 한번쯤 방문하여
            계약관계와 이사물량 등을 쌍방 확인하는 것이 좋다.
