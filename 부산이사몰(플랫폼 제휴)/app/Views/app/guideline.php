        <h3>2. 이사업체를 선정 할때는....</h3>
        허가업체와 무허가 업체를 일반 소비자들이 구분하는 것은 매우 어려운 일이다.
        이사화물 취급업체에서 소비자들의 선택을 받기 위한 일반 광고선전물을 제작할때 허가업체는 자신이 이사화물 취급 허가업체임을
        표기하는 경우가 있으나 확인하기가 쉽지않고 유ㆍ무허가 업체 공히 광고 선전물의 내용이 유사하고 거의 같아 광고 선전물만 가지고
        허가업체와 무허가 업체를 구별하는 것은 불가능하다고 본다.
        <br>
        따라서 허가업체인지 무허가 업체인지를 손쉽게 확인하는 방법은 해당지역 화물운송주선협회에 문의하거나
        이삿짐 견적 또는 계약시 관련 근거서류(운송주선사업 허가증 사본) 제시를 요구하는 방법으로 유.무허가 업무를
        직접 확인하는 방법이 있다.
        <br>
        <br>
        <h4>허가 업체와 무허가 업체의 차이점</h4>
        허가업체는 화물자동차운수사업법 제2조3호에 의해 이사화물운송주선사업 허가를 정부로부터 득한 사업자로서 허가 기준에 따라
        500만원 이상의 피해보상 이행보증보험에 가입하고 이사화물을 전문적으로 취급하는 2명이상의 상용인부를 고용한 업체를 말하며,
        『무허가 업체는 말 그대로 정부의 허가를 득하지 않고 이사화물을 취급하는 사업자로서 보통 소형화물차량(개인용달 또는 개별화물)
        1대를 소유하고 있거나 차량 소유 없이 일용직 인부만을 고용한 후 이삿짐을 취급하는 사업자』를 말한다.
        <br>
        따라서 허가업체와 무허가 업체의 차이점은 사업자의 사업규모, 서비스의 질적 차이를 떠나 이삿짐의 파손, 분실, 도난 등의
        피해가 발생할 시 무허가 업체는 그 책임 소재를 규명하기가 어려워 피해보상을 받기가 쉽지 않다는데 큰 차이점이 있다.
