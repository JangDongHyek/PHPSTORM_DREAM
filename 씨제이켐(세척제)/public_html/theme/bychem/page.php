	<p>1일 8시간 작업 동안 폭로된 유해물질의 시간가중평균농도 상한치</p>
	<h1 style="font-size: +1.2em; margin-top:6px;">3. STEL ( Short Term Exposure Limit 단기 폭로 한계 )</h1>
	<p>작업자가 15분간 연속하여 노출되어도 영향을 일으키지 않는 유해물질의 최고 농도</p>

