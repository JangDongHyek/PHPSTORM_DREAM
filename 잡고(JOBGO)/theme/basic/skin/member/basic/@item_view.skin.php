	$( window ).scroll( function() {
	  if ( $( document ).scrollTop() > jbOffset.top ) {
		$( '.fix_info' ).addClass( 'fixed_layer' );
		$( '.fix_info' ).css('top','100px');
		$( '.fix_info' ).css('transition','all 0.2s');
	  }
	  else {
		$( '.fix_info' ).removeClass( 'fixed_layer' );
		$( '.fix_info' ).css('top','200px');
		$( '.fix_info' ).css('transition','all 0.8s');
	  }
	});
  } );
</script>