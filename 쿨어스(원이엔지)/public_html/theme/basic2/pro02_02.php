                <h6>관련 용어</h6>
                <p>탄소집약도 (CII, Carbon Intensity indicator)  :  화물 운반량 및 운반거리당 CO2 배출량</p>
                <p>Gt (gross ton) : 선박의 크기를 나타내는 지표. 선적가능 용적을 톤으로 환산한 값 (100m3=1ton)</p>
                <p>TEU (Twenty foot Equivalent Units) : 20ft의 표준 컨테이너 크기를 나타내는 단위. 20ft 컨테이너 하나를 1TEU라고 함</p>
                <p>배출권거래제 (ETS, Emission Trading System) : 전체 배출총량을 감축하기 위하여 권역 내 배출원에 대하여 배출총량 일부를 할당하고 할당량 내에서만 배출하도록 제한</p>
                <p>ZEMBA ( Zero Emisson Marritime Buyere Alliance ) : 2023년 3월 아마존, 파타고니아, 치보, 아스펜연구소가 국제해운 무탄소 연료 시장 활성화를 위해 설립한 비영리단체</p>
            </div>

