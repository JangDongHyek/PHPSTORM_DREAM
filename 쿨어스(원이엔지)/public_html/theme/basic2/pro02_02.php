                    <p>Zero-emission 연료(메탄올 , 암모니아) 를 적용하는 경우보다 더 경제성 있는  선박탄소포집장치 기술인 것으로 평가 됨.</p>
                    <p>화석연료 연소 후 발생 CO2를 후처리(After Treatment) 기술이므로 배가스처리시스템 방식으로 설치될 것이므로,<br>해운부문 탈탄소 규제를 염두하여 설계되지 않았던 기존의 화석연료 선박에서도 본 기술을 적용하여 엄격한 규제 대응이 가능</p>
                    <p>국제해운에 대한 배출규제를 담당하는 IMO와 선박에 대한 배출규제를 시작하고 있는 유럽연합(EU)에서 MCCS(Marine Carbon Capture System) 적용시 감축성과를 인정할 수 있는 원칙과 방법을  반영하고 있음.</p>
