<p><strong>제 10 조 (판매회원의 판매활동)</strong></p>
<div>1. <strong>스마트 정비 자체 마켓 또는 스마트 정비 제휴 마켓 등에</strong> 상품 및 용역의 판매는 판매회원 등록이 완료됨과 동시에 가능하며, 이를 위해서 판매회원은 상품 및 용역에 관한 정보와 거래조건에 관한 내용을 전자상거래등에서의소비자보호에관한법률 등 관련 법령 및 이 약관에서 정한 방법으로 판매회원거래관리시스템(회사가 판매회원에 대한 정보제공 등을 목적으로 마련한 온라인상의 판매회원 전용화면을 말하며, 이하 &ldquo;<strong>스마트 정비 판매 등록시스템</strong>&rdquo; 또는 &ldquo;<strong>판매회원 화면</strong>&rdquo;이라 함)을 통하여 직접 등록, 관리하여야 합니다. 이때 판매하는 상품 및 용역의 종류와 범위, 판매가격, 거래조건은 판매서비스이용료, 배송비, 각종 부가서비스이용료 등을 고려하여 판매회원이 스스로 결정합니다. 그리고 회사는 상품대금 결제 방식, 상품배송 방식 등 구매자 피해방지에 관한 사항을 약관 등으로 정할 수 있습니다.<br>
    1의1. 판매회원은 등록 상품에 특별한 거래조건이 있거나 추가되는 비용이 있는 경우 구매자가 이를 알 수 있도록 명확하게 기재하여야 합니다.<br>
    1의2. 판매회원은 상품 등록 시 ① 품목별 재화 등에 관한 정보, ② 거래조건에 관한 정보 등 &lsquo;전자상거래 등에서의 상품 등의 정보제공에 관한 고시&rsquo;(공정거래위원회 고시 제2012-49호, 이하 &lsquo;상품정보제공고시&rsquo;)에서 정한 정보를 입력하여야 하고, 상품 등록 후 상품정보제공고시가 변경되는 경우 그에 맞추어 관련 정보를 수정, 보완하여야 합니다. 판매회원이 상품정보제공고시에 따른 정보를 입력하지 않거나, 상품 등록 후 변경된 상품정보제공고시에 따라 정보를 수정, 보완하지 않는 경우 회사는 신용점수 차감, 상품 판매 제한, 판매회원 ID 중지 등 필요한 조치를 취할 수 있습니다.<br>
    1의3. 회사는 상품검색의 효율성, 판매회원관리시스템의 정상적인 운영을 위하여, 판매회원에 대한 사전통지로써 판매자 1인당 물품 등록 건수를 제한할 수 있습니다. 구체적인 제한시기, 내용, 방법 등은 판매회원이 알 수 있도록 사전에 판매회원 화면을 통해 게시합니다.&nbsp;판매회원이 회사의 물품등록건수 제한 조치를 위반한 경우 회사는 상품등록을 삭제하거나 취소할 수 있고, 신용점수 차감, 상품판매제한, 판매회원 ID 중지 등 필요한 조치를 취할 수 있습니다.<br>
    1의4. 판매회원은 상품등록 후 6개월간 구매이력이 없는 상품등록(이하 &ldquo;휴면리스팅&rdquo;)에 대해서는 이를 유지할 수 없고, 회사는 이러한 휴면리스팅에 대해서는 품절처리, 등록취소 등 적절한 조치를 취할 수 있습니다.<br>
    2. 판매회원은 재고 수량 등 수시로 변동되는 사항에 대한 데이터를 적절히 관리하여야 하며, <strong>스마트 정비 판매 등록시스템</strong> 에 데이터를 허위로 기재할 수 없습니다.<br>
