    10. 회사는 소비자의 악성 리뷰에 대한 삭제 또는 임시조치 기준, 악성 리뷰 작성 소비자에 대한 이용 제한 기준, 판매 회원의 리뷰 게시 중단 요청 절차 등의 구체적인 내용 및 절차를 마련한 후 다양한 방식으로 제공합니다. 또한, 이에 대한 판매회원의 문의시 성실히 설명합니다.
