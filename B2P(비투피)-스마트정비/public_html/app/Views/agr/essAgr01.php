<p><strong>제 9 조 (제공 서비스)</strong></p>
<div>1. 회사는 판매회원이 등록한 상품에 대한 판매촉진을 위해 자체 전자상거래 플랫폼 또는 국내외 포털 사이트, 또는 제휴사가 운영하는 사이트 및 제휴사와 제휴하는 국내외 사이트 등 회사가 계획한 사이트 등에 노출할 수 있으며, 할인행사 등을 실시할 경우에 등록된 상품을 할인된 가격으로 노출할 수 있습니다.<br>
