    <strong>3) </strong><strong>판매확대 연계지원 서비스 : 스마트 정비 마켓의 판매회원으로 등록한 사업자는 회사가 판매확대를 위해 스마트 정비 제휴 마켓을 통해 상품 매출을 증대할 수 있도록 판매를 지원하는 서비스를 말합니다.<br>
