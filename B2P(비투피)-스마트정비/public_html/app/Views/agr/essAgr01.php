<p><strong>제 4 조 (대리행위 및 보증의 부인)</strong></p>
<div>1. 회사는 통신판매중개자로서 효율적인 서비스를 위한 시스템 운영 및 관리 책임 및 <strong>판매확대 연계지원 만을 수행하며, </strong>재화 또는 용역의 거래와 관련하여 구매자 또는 판매회원을 대리하지 아니하고, 회원 사이에 성립된 거래 및 회원이 제공하고 등록한 정보에 대해서는 해당 회원이 그에 대한 직접적인 책임을 부담하여야 합니다.<br>
    2. 회사는 회사가 제공하는 시스템을 통하여 이루어지는 구매자와 판매회원 간의 거래와 관련하여 판매의사 또는 구매의사의 존부 및 진정성, 등록물품의 품질, 완전성, 안정성, 적법성 및 타인의 권리에 대한 비침해성, 구매자 또는 판매회원이 입력하는 정보 및 그 정보를 통하여 링크된 URL에 게재된 자료의 진실성 또는 적법성 등 일체에 대하여 보증하지 아니하며, 이와 관련한 일체의 위험과 책임은 <strong>해당 구매자와 판매회원</strong>이 전적으로 부담합니다. </div>
<p><strong>제 5 조 (판매 및 </strong><strong>판매확대 연계지원 서비스 </strong><strong>이용계약의 성립)</strong></p>
<div>1. 판매 및 판매확대 연계지원 서비스 이용계약(이하 &quot;이용계약&quot;이라고 합니다)은 회사가 제공하는 판매 및 판매확대 연계지원 서비스 서비스를 이용하고자 하는 자의 이용신청에 대하여 회사가 이를 승낙함으로써 성립합니다. 회사는 이용승낙의 의사표시를 해당 서비스화면에 게시하거나 e-mail 또는 기타 방법으로 통지합니다.<br>
    2. 판매 및 판매확대 연계지원 서비스 서비스를 이용하고자 하는 자는 본 약관에 동의하고, 회사가 정하는 회원 가입 신청 양식에 따라 필요한 사항을 기입합니다.<br>
    3. 회원가입은 만 14세 이상의 개인 또는 사업자(개인사업자 및 법인사업자)가 할 수 있으며, 이용신청자는 실명으로 가입신청을 해야 하며, 실명이 아니거나 타인의 정보를 도용하는 경우 서비스이용이 제한되거나 관련 법령에 의거 처벌받을 수 있습니다.<br>
    4. 이용신청의 처리는 신청순서에 의하며, 회원가입의 성립시기는 회사의 승낙이 회원에게 도달한 시점으로 합니다.<br>
    5. 회사는 다음과 같은 사유가 발생한 경우 이용신청에 대한 승낙을 거부하거나 유보할 수 있습니다.<br>
    1) 회사의 실명확인절차에서 본인의 실명으로 가입 신청하지 않은 것이 확인된 경우<br>
    2) 이미 가입된 회원과 이름 및 주민등록번호(또는 사업자등록번호)가 동일한 경우<br>
    3) 회사에 의하여 이용계약이 해지된 날로부터 2개월 이내에 재이용신청을 하는 경우<br>
    4) 회사로부터 회원자격 정지 조치 등을 받은 회원이 그 조치기간 중에 이용계약을 임의 해지하고 재이용신청을 하는 경우<br>
    5) 설비에 여유가 없거나 기술상 지장이 있는 경우<br>
    6) 기타 이 약관에 위배되거나 위법 또는 부당한 이용신청임이 확인된 경우 및 회사가 합리적인 판단에 의하여 필요하다고 인정하는 경우 </div>
<p><strong>제 6 조 (증빙서류)</strong></p>
<div>회사는 판매회원이 이 약관 제5조 제2항에 따라 제공한 정보의 정확성을 확인하기 위하여 관련법령이 허용하는 범위 내에서 증빙자료의 제공을 요청할 수 있습니다., 판매회원이 부당하게 증빙자료를 제공하지 않는 경우 회사는 이용계약의 해지, 판매 판매 활동 및 <strong>판매확대 연계지원 서비스의 제한</strong> 또는 정산금의 지급 보류 등 조치를 취할 수 있으며, 판매회원은 이로 인하여 발생하는 손해에 대해 회사에게 어떠한 책임도 물을 수 없습니다. </div>
<p><strong>제 7 조 (아이디 및 비밀번호의 관리)</strong></p>
<div>1. 아이디(ID) 및 비밀번호에 대한 관리책임은 <strong>구매자 및 판매회원에게 있으며</strong>, 회원 및 판매회원은 어떠한 경우에도 본인의 아이디(ID) 또는 비밀번호를 타인에게 양도하거나 대여할 수 없습니다.<br>
    2. 회사의 귀책사유 없이 아이디(ID) 또는 비밀번호의 유출, 양도, 대여로 인하여 발생하는 손실이나 손해에 대하여는 회원 및 이용자 본인이 그에 대한 책임을 부담합니다.<br>
    3. <strong>구매자 및 판매회원은</strong> 아이디(ID) 또는 비밀번호를 도난 당하거나 제3자가 무단으로 이를 사용하고 있음을 인지한 경우, 이를 즉시 회사에 통보하여야 하고 회사는 이에 대한 신속한 처리를 위하여 최선의 노력을 다합니다. </div>
<p><strong>제 8 조(계약기간 및 이용계약의 종료)</strong></p>
<div>1. 이용계약의 기간은 판매회원이 약관에 대해 동의한 날로부터 당해 연도 말일까지로 하고, 기간 만료 1개월 전까지 서면에 의한 반대의 의사표시가 없는 한 계약기간은 동일한 조건으로 1년간 자동 갱신됩니다.<br>
    2. 회사의 해지<br>
    1) 회사는 다음과 같은 사유가 발생하거나 확인된 경우 이용계약을 해지할 수 있습니다<br>
    ① 다른 회원 또는 타인의 권리나 명예, 신용 기타 정당한 이익을 침해하거나 대한민국 법령 또는 공서양속에 위배되는 행위를 한 경우<br>
    ② 사이트 이용 시 발생된 문제를 이유로 상식적인 범위를 벗어난 과도한 금전보 상이나 특혜를 요구하거나, 주문거래와 상관없이 회사와 그 직원을 괴롭히기 위한 목적으로 고의로 민원을 제기하거나 업무를 방해하는 행위를 하는 등 회사가 제공하는 서비스의 원활한 진행을 방해하는 행위를 한 경우<br>
    ③ 제5조 제5항의 승낙거부사유가 있음이 확인된 경우<br>
    ④ 판매회원이 제공한 정보 또는 그에 관한 증빙자료가 허위이거나 회사가 요청하는 증빙자료를 제공하지 않는 경우<br>
    ⑤ 기타 회사가 이 약관에서 정한 해지사유가 발생한 경우<br>
