    3의2. 회사는 이 약관에 따라 판매회원에게 제공하는 통신판매중개서비스 및 <strong>판매확대 연계지원 서비스의</strong> 를 제한하거나 중지하려는 경우에는 제한 또는 중지 예정일의 7일 전까지 그 이유 및 내용을 구체적으로 고지합니다. 다만, 불법행위, 소비자 피해 발생 우려 등 긴급하고 상당한 이유가 있는 경우에는 위 예고 기간 중에도 해당 회원의 서비스 이용을 제한할 수 있습니다.
