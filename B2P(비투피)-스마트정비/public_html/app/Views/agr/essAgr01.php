    </strong>5) 할인쿠폰: 회사의 서비스를 통하여 물품을 구매할 때 표시된 금액 또는 비율만큼 물품대금에서 할인 받을 수 있는 회사 전용의 사이버 또는 오프라인 쿠폰을 말하며, 아이템할인, 에누리쿠폰(구매쿠폰)과 바이어쿠폰을 통칭합니다.<br>
