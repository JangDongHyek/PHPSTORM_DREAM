    4. 판매회원은 전자상거래등에서의 소비자보호에 관한 법률(이하 &quot;전소법&quot;이라 함) 등 <strong>스마트 정비 마켓과 모바일 스마트 정비 마켓</strong>에서의 상품 및 용역의 판매와 관련하여 법령이 요구하는 사항을 준수하여야 합니다.<br>
    5. 판매회원은 회사의 서면에 의한 사전 승인 없이 <strong>스마트 정비 마켓과 모바일 스마트 정비 마켓 </strong>상호나 로고 등을 사용할 수 없습니다.<br>
    6. 판매회원은 <strong>스마트 정비 자체 마켓과 스마트 정비 제휴 마켓을 </strong>통하지 않고 구매자에게 직접 상품을 판매하거나 이를 유도하여서는 아니 되며, 이를 위반할 경우 회사는 당해 판매회원을 탈퇴시킬 수 있습니다.<br>
    7. 판매회원은 판매된 상품 및 용역에 대한 보증 서비스를 자신의 책임과 비용으로 실시하여야 합니다.<br>
    8. 판매회원은 <strong>회사가 제공하는 자체 전자상거래 플랫폼(스마트 정비 자체 마켓)을 이용할 경우</strong> 자신의 판매회원 정보란에 회사가 정하는 절차에 따라 인증받은 유선전화 또는 휴대폰 번호를 대표번호로서 설정하고 항상 최신 정보로서 유지하여야 합니다. 또한, 대표번호 변경시 또는 매 6개월마다 새로이 인증을 받아야 합니다. 판매회원이 본항의 의무를 이행하지 않을 경우 회사는 이행 완료시까지 해당 판매회원의 물품 등록과 편집을 제한할 수 있습니다.<br>
    <br>
    9. 대체텍스트 및 OCR 적용<br>
