<p><strong>제 12 조 (상품의 배송)</strong></p>
<div>1. 구매자의 주문에 따른 결제가 완료되면, 회사는 판매회원이 주문 정보를 확인할 수 있도록 조치를 취하고, 판매회원은 당해 주문 정보에 따라 배송을 하여야 합니다.<br>
    2. 판매회원은 주문내역을 확인하고 배송 중 상품이 파손되지 않도록 적절한 포장을 한 후 배송의 증명 또는 추적이 가능한 물류대행(택배)업체에 배송을 위탁하여야 합니다.<br>
    3. 전소법 제15조 1항에 의거하여 판매회원은 구매자의 결제일로부터 3 영업일 이내에 상품의 발송을 완료하여야 하고, <strong>스마트정비 판매등록시스템</strong>에 송장번호 등의 발송 관련 데이터를 입력하여 발송이 완료되었음을 증명하여야 합니다.<br>
    4. 판매회원이 전항의 기한 내에 발송하지 않은 경우 상품 판매 제한 또는 옵션 판매 제한, 물품 및 옵션 등록 가능개수 제한 등의 패널티를 받을 수 있습니다. 또한, 구매자의 귀책사유로 인하지 않은 배송지 오류 등으로 인하여 구매자가 상품을 정상적으로 수령하지 못한 경우 판매회원은 그에 관한 모든 책임을 부담하여야 합니다.<br>
    5. 판매회원은 발송 후 3일 이내에 구매자가 주문한 상품을 수령할 수 있도록 조치해야 합니다.<br>
    6. 회사는 제3자와 업무제휴를 통해 통합택배, 해외배송 서비스 등을 실시할 수 있습니다. </div>
<p><strong>제 13 조 (판매회원의 배송 의무)</strong></p>
<div>1. 주문을 확인한 판매회원은 <strong>스마트정비 판매등록시스템을</strong> 통하여 발송예정일 및 발송과 관련된 데이터를 입력하여야 합니다.<br>
    2. 판매회원은 상품을 발송하기 전에 주문확인을 통하여 반드시 주문이 취소되었는지 최종 확인하여야 하며 상품발송 후 주문확인 미입력 건에 대해서 구매자가 <strong>스마트정비 자차마켓 또는 스마트정비 제휴마켓으로 취소요청을 </strong>하는 경우 판매회원에게 통보 없이 주문이 취소되어 구매자에게 환불될 수 있으며 상품회수에 소요되는 비용은 판매회원의 부담이 됩니다.<br>
    3. 주문이 확인된 후, 발송예정일을 입력하지 않은 상태로 2주 이상 경과시 주문 건은 자동 환불될 수 있습니다.<br>
    4. 상품을 발송하지 않아 거래가 취소되는 경우 회사는 판매회원에게 판매거부로 인한 불이익을 줄 수 있습니다. </div>
<p><strong>제 14 조 (판매회원의 배송정보 입력 의무 및 분쟁 처리 의무)</strong></p>
<div>1. 판매회원은 상품을 물류대행업체에 배송 위탁한 후, 즉시 <strong>스마트정비 판매시스템에</strong> 송장번호 등 상품 발송과 관련된 데이터를 입력하여 발송이 완료되었음을 증명하여야 합니다. 판매회원은 실제 배송대행업체에 배송 위탁한 상품에 한해 <strong>스마트정비 판매시스템에</strong> 발송과 관련된 데이터를 입력하여야 하며, 실제 배송위탁이 이루어지지 않은 상품의 배송정보를 입력한 경우 회사는 허위배송정보를 입력한 것으로 간주합니다. 판매회원이 <strong>스마트정비 판매시스템에</strong> 발송과 관련된 데이터를 입력하지 않았거나 부정확한 정보 입력 등으로 배송증명이나 추적이 불가능한 경우 회사는 구매자의 요청에 따라 주문을 취소하고 구매자에게 환불할 수 있습니다.<br>
