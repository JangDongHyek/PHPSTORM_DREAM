    7) 에누리쿠폰(구매쿠폰): 구매자가 회사의 서비스를 통하여 물품을 구매할 때 표시된 금액 또는 비율만큼 물품대금에서 할인 받을 수 있는 회사 전용의 사이버 또는 오프라인 쿠폰을 말합니다. 회사는 판매회원의 동의(승인을 포함하며 이하 같다)가 있는 경우에 한하여 에누리쿠폰(구매쿠폰)이 적용된 물품판매거래를 중개할 수 있으며, 판매회원의 동의가 없는 경우에는 당해 거래를 신속히 취소 처리합니다. 에누리쿠폰(구매쿠폰)으로 인한 판매가격 할인액 상당은 회사가 판매회원으로부터 수령할 서비스이용료에서 직접 차감됩니다.<br>
    8) 바이어쿠폰: 구매자가 회사의 서비스를 통하여 물품을 구매할 때 표시된 금액 또는 비율만큼 물품대금에서 할인 받을 수 있는 회사 전용의 사이버 또는 오프라인 쿠폰을 말합니다. 회사는 판매회원의 동의없이 구매자에게 바이어쿠폰을 발행할 수 있으며, 쿠폰에 표시된 할인금액 및 할인비율만큼 판매가격이 할인되는 경우 그 할인액 상당은 회사가 판매회원으로부터 수령할 서비스이용료에서 직접 차감됩니다.<br>
